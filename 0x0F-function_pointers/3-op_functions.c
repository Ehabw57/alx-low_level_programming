#include "3-calc.h"

/**
 * op_add - adds tow nums
 * @a: the first num
 * @b: the second num
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub tow nums
 * @a: the first num
 * @b: the sec num
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul tow nums
 * @a: the first num
 * @b: the sec num
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div tow nums
 * @a: the first num
 * @b: the sec num
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod tow nums
 * @a: the first num
 * @b: the sec num
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
